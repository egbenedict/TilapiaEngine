    def __init__(self, color):
        self.color = color # 1 = white, -1 = black
        self.id = "rook"
        self.base_value = 500

    def __str__(self):
        return "♖" if self.color == 1 else "♜"

    def get_bonus(self, i, endgame):
        if endgame:
            return Rook.square_bonuses[self.color][1][i]
        else:
            return Rook.square_bonuses[self.color][0][i]